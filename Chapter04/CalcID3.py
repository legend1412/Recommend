import math

a = (-7 / 12) * math.log2(7 / 12) + (-5 / 12) * math.log2(5 / 12)
a = round(a, 4)
print('总熵:{:.4f}'.format(a))

print('计算天气的信息增益')
b = (5 / 12) * ((-3 / 5) * math.log2(3 / 5) + (-2 / 5) * math.log2(2 / 5))
print('晴天熵:{:.4f}'.format(b))
c = (3 / 12) * ((-2 / 3) * math.log2(2 / 3) + (-1 / 3) * math.log2(1 / 3))
print('阴天熵:{:.4f}'.format(c))
d = (4 / 12) * ((-2 / 4) * math.log2(2 / 4) + (-2 / 4) * math.log2(2 / 4))
print('雨天熵:{:.4f}'.format(d))
print('天气熵:{:.4f}'.format(b + c + d))
print('天气信息增益:{:.4f}'.format(a - (b + c + d)))

print('计算温度的信息增益')
b = (4 / 12) * ((-2 / 4) * math.log2(2 / 4) + (-2 / 4) * math.log2(2 / 4))
print('炎热熵:{:.4f}'.format(b))
c = (4 / 12) * ((-3 / 4) * math.log2(3 / 4) + (-1 / 4) * math.log2(1 / 4))
print('寒冷熵:{:.4f}'.format(c))
d = (4 / 12) * ((-2 / 4) * math.log2(2 / 4) + (-2 / 4) * math.log2(2 / 4))
print('适中熵:{:.4f}'.format(d))
print('温度熵:{:.4f}'.format(b + c + d))
print('温度信息增益:{:.4f}'.format(a - (b + c + d)))

print('计算湿度的信息增益')
b = (5 / 12) * ((-2 / 5) * math.log2(2 / 5) + (-3 / 5) * math.log2(3 / 5))
print('高湿度熵:{:.4f}'.format(b))
c = (7 / 12) * ((-5 / 7) * math.log2(5 / 7) + (-2 / 7) * math.log2(2 / 7))
print('正常湿度熵:{:.4f}'.format(c))
print('湿度熵:{:.4f}'.format(b + c))
print('湿度信息增益:{:.4f}'.format(a - (b + c)))

print('计算风速的信息增益')
b = (5 / 12) * ((-2 / 5) * math.log2(2 / 5) + (-3 / 5) * math.log2(3 / 5))
print('强风速熵:{:.4f}'.format(b))
c = (7 / 12) * ((-5 / 7) * math.log2(5 / 7) + (-2 / 7) * math.log2(2 / 7))
print('弱风速熵:{:.4f}'.format(c))
